#   33、元素分类
#       有如下值li = [11, 22, 33, 44, 55, 66, 77, 88, 99, 90]，将所有大于66的值保存至字典的第一个key中，
#       将小于66的值保存至第二个key的值中。
#       即： {'k1': 大于66的所有值列表, 'k2': 小于66的所有值列表}
dic = {"key1": [], 'key2': []}
li = [11, 22, 33, 44, 55, 66, 77, 88, 99, 90]
for i in li:
    if i > 66:
        dic["key2"].append(i)
    else:
        dic["key1"].append(i)
print(dic)